    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->validate($request,[
            'Week'=>'required'
        ]);
        $week=CurrentWeek::where('Week',$request->Week)->get();
        if($week->count()>0){
            Session::flash('error','Week Already Exists');
            return redirect()->back();
        }
        CurrentWeek::create([
            'Week'=>$request->Week
        ]);

        Session::flash('success','Current Week  Updated');
        return redirect()->back();
    }

